"""
Write a program to check a character is vowel or not
"""
alp= input("Enter an alphabet: ")
if alp=="a":
    print("It is a vowel letter")
elif alp=="e":
    print("It is a vowel letter")
elif alp=="i":
    print("It is a vowel letter")
elif alp=="o":
    print("It is a vowel letter")
elif alp=="u":
    print("It is a vowel letter")
