"""
Write a program to accept a number from 1 to 12 and display name of the month like 1 for January,
2 for February and so on
"""
num= int(input("Enter a number: "))
if num==1:
        print("1=January")
elif num==2:
        print("2=February")
elif num==3:
        print("3=March")
elif num==4:
        print("4=April")
elif num==5:
        print("5=May")
elif num==6:
        print("6=June")
elif num==7:
        print("7=July")
elif num==8:
        print("8=August")
elif num==9:
        print("9=September")
elif num==10:
        print("10=October")
elif num==11:
        print("11=November")
elif num==12:
        print("December")
else:
    print("Error")
    