# 98.	Write a program to enter the numbers till the user wants and at the end 
# it should display the sum of all the numbers entered. 

number= int(input("Enter a number upto which you want the sum of: "))
sum= number
for i in range(0,sum):
    sum= sum+i
print(sum)

